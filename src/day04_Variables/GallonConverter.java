package day04_Variables;

public class GallonConverter {

    public static void main(String[] args) {

        int gallon = 1;
        double litter = 3.79 * gallon;

        System.out.println("1 gallon = " + litter + " litters");
        System.out.println("2 gallon = " + 2 * litter + " litters");
        System.out.println("3 gallon = " + 3 * litter + " litters");
        System.out.println("4 gallon = " + 4 * litter + " litters");
        System.out.println("5 gallon = " + 5 * litter + " litters");

    }

}
