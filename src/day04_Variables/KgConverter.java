package day04_Variables;

public class KgConverter {

    public static void main(String[] args) {

        double lbs = 1;
        double kg = 0.48 * lbs;

        System.out.println("1 lbs = " + kg + " kg");
        System.out.println("2 lbs = " + 2 * kg + " kg");
        System.out.println("3 lbs = " + 3 * kg + " kg");
        System.out.println("4 lbs = " + 4 * kg + " kg");
        System.out.println("5 lbs = " + 5 * kg + " kg");
        System.out.println("6 lbs = " + 6 * kg + " kg");
        System.out.println("7 lbs = " + 7 * kg + " kg");
        System.out.println("8 lbs = " + 8 * kg + " kg");
        System.out.println("9 lbs = " + 9 * kg + " kg");
        System.out.println("10 lbs = " + 10 * kg + " kg");

    }

}
