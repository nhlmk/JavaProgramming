package day07_Operators;

public class UnaryOperators {

    public static void main(String[] args) {

        int a = 5;
        // pre increment. increases value +1 right away. ++a.
        // --a decreases value immediately.
        // a++ post increment.
// a-- post

        System.out.println(--a);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a--);
    }



}
