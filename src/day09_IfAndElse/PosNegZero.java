package day09_IfAndElse;

public class PosNegZero {
    public static void main(String[] args) {

        int num1 = -3;

        if (num1 < 0) {
            System.out.println("it is negative");
        }

        if (num1 > 0) {
            System.out.println(" it is positive ");
        } else {
            System.out.println(" it is zero ");
        }
    }
}
