package day15_ForLoop;

public class forLoop {
    public static void main(String[] args) {
        for( int num1 = 2; num1<10; num1 += 3){
            System.out.println(num1);
        }

        System.out.println("====================================================");
        for(int i = 1; i<=10; i+=2){
            System.out.println(i);
        }


    }
}
